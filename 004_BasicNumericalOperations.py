'''
Basic Numerical Charectors:
+ = Plus
- = Minus
* = Multiply
/ = Devide
% = Modulas
// = Floor
** = Exponentital
'''

print(11+4)
print(11-4)
print(11*4)
print(11/4)
print(11%4)
print(11//4)
print(11**4)

